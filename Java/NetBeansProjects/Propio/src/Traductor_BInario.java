    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String texto = Texto.getSelectedText();
        String binario = Binario.getSelectedText();
        
        for(int i = 0; i <texto.length(); i++){
            Vector bin = new Vector();
            char palabra = texto.substring(i).charAt(0);
            int ascii = (int) palabra;
            do{
                bin.add(String.valueOf((ascii%2)));
                ascii = (int) ascii%2;
                
            }while(ascii>1);
            bin.add(1);
            String binarios = "";
            for(int j = bin.size(); j>=0; j--){
                binarios += String.valueOf(bin.get(j));
                
            }
            Texto_Binario.setText(binarios);
            
            
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

