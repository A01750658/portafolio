    private void Carta1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Carta1MouseClicked
        // TODO add your handling code here:
        r_color();
        Random r = new Random(); 
        int kenobi = r.nextInt(colores.size());
        Color_c i = (Color_c) colores.get(r.nextInt(kenobi));
        colores.remove(kenobi);
        carta_1 = i.getName();
        
        cont++;
        count ++;
        switch (carta_1) {
            case "BLUE":
                Carta1.setBackground(Color.blue);
                if(azul == true){
                    Carta1.setVisible(false);
                    change = true;
                }
                else{
                    azul = true;                 
                }               
                break;
            case "RED":
                Carta1.setBackground(Color.red);
                if(rojo == true){
                    Carta1.setVisible(false);
                    change = true;
                }
                else{
                    rojo = true;
                    
                }
                break;
            case "GREEN":
                Carta1.setBackground(Color.green);
                if(verde == true){
                    Carta1.setVisible(false);
                    change = true;
                }
                else{
                    verde = true;
                }
                
                break;
            default:
                Carta1.setBackground(Color.yellow);
                if(amarillo == true){
                    Carta1.setVisible(false);
                    change = true;
                }
                else{
                    amarillo = true;
                }
                
                break;
        }
        
        if(change == true){
            Carta1.setVisible(false);
        }
        check.setText(String.valueOf(count));
        if(count >= 2){
            Carta1.setBackground(Color.gray);
            Carta2.setBackground(Color.gray);
            Carta3.setBackground(Color.gray);
            Carta4.setBackground(Color.gray);
            Carta5.setBackground(Color.gray);
            Carta6.setBackground(Color.gray);
            Carta7.setBackground(Color.gray);
            Carta8.setBackground(Color.gray);
            count = 0;
            azul = false; 
            rojo = false; 
            verde = false; 
            amarillo = false; 
            change = false; 
          
        }
        
        
    }//GEN-LAST:event_Carta1MouseClicked

