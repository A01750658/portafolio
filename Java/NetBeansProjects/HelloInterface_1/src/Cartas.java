    public void r_color(){
       Random r = new Random();
       ArrayList <Integer> controles = new ArrayList<>();
       controles.add(6);
       int con = 0;
        String [] colors = {"BLUE", "RED", "GREEN", "YELLOW"};
        while(controles.size()<5){
            
            
            int random = r.nextInt(4);
            c.setName(colors[random]);
            colores.add(c);
            if(random != controles.get(con)){
                controles.add(random);
                con ++;
            }
            
            
        }
        
        
    }
